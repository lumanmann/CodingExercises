//a005: 矩形面積

/*給你一個矩形的長和寬，請問你它的面積是多少？
 Input：
 輸入兩個正整數 H、W，代表這個矩形的長和寬。
 Output：
 請輸出這個矩形的面積。
 
 Sample Input：
 輸入1: 1 2  輸入2: 2 3
 Sample Output ：
 輸出1: 2  輸出2: 6
 */

func area(H: Int, W : Int) -> Int {
    return H*W
}


area(H: 1, W: 2)
area(H: 2, W: 3)
